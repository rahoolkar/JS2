//switch statement in javascript

const fruit = "Mango";

switch (fruit) {
  case "Apple":
    console.log("apple is the answer");
    break;

  case "Orange":
    console.log("Orange is the fruit");
    break;

  case "Mango":
    console.log("Mongo is the fruit");
    break;

  default:
    console.log("default statement");
}
