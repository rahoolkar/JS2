//prefix and postfix in js
let x = 90 ;
let y = x++;
//here x value will be 91 and y value will be 90
console.log(x);
console.log(y);

let a = 15;
let b = ++a;
//here a value will be 16 and b value will be 16
console.log(a);
console.log(b);
